'''
07. Faça um Programa que calcule a área de um quadrado,
em seguida mostre o dobro desta área para o usuário.
'''

print('Digite a base e a altura de um quadrado para obter a sua area')
lado1 = float(input('Digite a base: '))
lado2 = float(input('Digite a altura: '))

area= lado1*lado2

print(f'A area do quadrilatero é {area} e o dobro dela é igual a {area*2}')
